
// se define las variables y se ingresa por pantalla el valor
let temperaturaCelsius = parseInt(prompt("Ingresa la temperatura en grados Celsius:"));
// se define la variable que hara la convercion con el calculo
let temperaturaKelvin = temperaturaCelsius + 273.15;
let temperaturaFahrenheit = (temperaturaCelsius * 9/5) + 32;
// se imprime por pantallla el resultado de dicha convercion por consola del navegador
console.log("Temperatura en grados Celsius: " + temperaturaCelsius + "°C");
console.log("Temperatura en grados Kelvin: " + temperaturaKelvin + " K");
console.log("Temperatura en grados Fahrenheit: " + temperaturaFahrenheit + " °F");






